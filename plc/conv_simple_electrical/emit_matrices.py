def build_evidence_matrix():
    """Build conductor-by-conductor evidence matrix, one table per sheet."""

    lines = [
        "# EVIDENCE MATRIX",
        "",
        f"> {HEADER}",
        "",
    ]

    # Process each sheet (E-003, E-005, E-006, E-007)
    for sheet_id in ["E-003", "E-005", "E-006"]:
        # Get wires for this sheet
        sheet_wires = [w for w in wires if w.get("sheet") == sheet_id]
        sheet_wires_sorted = sorted(sheet_wires, key=lambda x: x.get("proposed_number", ""))

        lines.append(f"## {sheet_id}")
        lines.append("")
        lines.append(
            "| Wire | From | To | Signal | Type | Status | Source | Notes | Rendered | Tagged |"
        )
        lines.append(
            "|------|------|----|---------|---------|----|--------|-------|----------|--------|"
        )

        verified_count = 0
        field_verify_count = 0
        proposed_count = 0
        missing_source_verified = 0
        rendered_solid_not_verified = 0

        for wire in sheet_wires_sorted:
            wire_num = wire.get("proposed_number", "?")
            from_ep = wire.get("from", "?")
            to_ep = wire.get("to", "?")
            signal = wire.get("signal", "")
            wire_type = wire.get("type", "")
            status = wire.get("status", "")
            source = wire.get("source", "")
            notes = wire.get("note", "")

            # Count statuses
            if status == "verified":
                verified_count += 1
                if not source:
                    missing_source_verified += 1
            elif status == "field_verify":
                field_verify_count += 1
            elif status == "proposed":
                proposed_count += 1

            # Check rendering (all renderers tag conductors with data-wire as of V3)
            svg_data = svg_wires.get(sheet_id, {}).get(wire_num)
            if svg_data:
                rendered = svg_data["rendered"]
                tagged = "yes"
                # Check for logic error: rendered solid but not verified
                if rendered == "solid" and status != "verified":
                    rendered_solid_not_verified += 1
            else:
                rendered = "absent"
                tagged = "no"

            lines.append(
                f"| {wire_num} | {from_ep} | {to_ep} | {signal} | {wire_type} | {status} | "
                f"{source[:20] if source else ''} | {notes[:30] if notes else ''} | {rendered} | {tagged} |"
            )

        lines.append("")
        lines.append("### SUMMARY")
        lines.append(f"- Total wires: {len(sheet_wires_sorted)}")
        lines.append(f"- Verified: {verified_count}")
        lines.append(f"- Field verify: {field_verify_count}")
        lines.append(f"- Proposed: {proposed_count}")
        if missing_source_verified > 0:
            lines.append(
                f"- **Verified wires missing source: {missing_source_verified}** (should be 0)"
            )
        if rendered_solid_not_verified > 0:
            lines.append(
                f"- **Rendered solid but not verified: {rendered_solid_not_verified}** (should be 0)"
            )
        lines.append("")

    # E-007 RS-485 links
    lines.append("## E-007 RS-485 / Modbus RTU")
    lines.append("")
    lines.append(
        "| Wire | From | To | Signal | Type | Status | Source | Notes | Rendered | Tagged |"
    )
    lines.append(
        "|------|------|----|---------|---------|----|--------|-------|----------|--------|"
    )

    e007_links_sorted = sorted(e007_links, key=lambda x: x.get("wire_label", ""))
    verified_count_e007 = 0
    field_verify_count_e007 = 0

    for link in e007_links_sorted:
        wire_label = link.get("wire_label", "?")
        src_device = link.get("src_device", "?")
        src_terminal = link.get("src_terminal", "?")
        dst_device = link.get("dst_device", "?")
        dst_terminal = link.get("dst_terminal", "?")
        from_ep = f"{src_device}.{src_terminal}"
        to_ep = f"{dst_device}.{dst_terminal}"
        signal = link.get("signal", "")
        cable = link.get("cable", "")
        wire_type = cable
        status = link.get("evidence", "")
        source = link.get("source", "")
        notes = link.get("notes", "")

        if status == "verified":
            verified_count_e007 += 1
        elif status == "field_verify":
            field_verify_count_e007 += 1

        svg_data = svg_wires.get("E-007", {}).get(wire_label)
        if svg_data:
            rendered = svg_data["rendered"]
            tagged = "yes"
        else:
            rendered = "absent"
            tagged = "no"

        lines.append(
            f"| {wire_label} | {from_ep} | {to_ep} | {signal} | {wire_type} | {status} | "
            f"{source[:20] if source else ''} | {notes[:30] if notes else ''} | {rendered} | {tagged} |"
        )

    lines.append("")
    lines.append("### SUMMARY")
    lines.append(f"- Total links: {len(e007_links_sorted)}")
    lines.append(f"- Verified: {verified_count_e007}")
    lines.append(f"- Field verify: {field_verify_count_e007}")
    lines.append("")

    return "\n".join(lines)


