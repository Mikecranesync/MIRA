#!/usr/bin/env python3
"""
Emit three markdown matrices from electrical model YAMLs and SVG sheets.
Pure Python (stdlib + pyyaml); run with `py -3 emit_matrices.py`.
Deterministic output: sorted rows, no timestamps.
"""

import re
import sys
from collections import defaultdict
from pathlib import Path

try:
    import yaml
except ImportError:
    print("ERROR: pyyaml not found. Install with: pip install pyyaml", file=sys.stderr)
    sys.exit(1)

# Paths
SCRIPT_DIR = Path(__file__).parent
MODEL_DIR = SCRIPT_DIR / "model"
SHEETS_DIR = SCRIPT_DIR / "sheets"
REVIEW_DIR = SCRIPT_DIR / "review"

# Create review dir
REVIEW_DIR.mkdir(exist_ok=True)

HEADER = "Generated by emit_matrices.py — do not hand-edit; regenerate after any model change."

# ============================================================================
# Load all YAML files
# ============================================================================


def load_yaml(path):
    """Load YAML file; return {} if missing with warning."""
    if not path.exists():
        print(f"[WARN] {path.name} not found", file=sys.stderr)
        return {}
    with open(path, "r", encoding="utf-8") as f:
        return yaml.safe_load(f) or {}


devices_data = load_yaml(MODEL_DIR / "devices.yaml")
terminals_data = load_yaml(MODEL_DIR / "terminals.yaml")
wires_data = load_yaml(MODEL_DIR / "wires.yaml")
sheets_data = load_yaml(MODEL_DIR / "sheets.yaml")
open_items_data = load_yaml(MODEL_DIR / "open_items.yaml")
e007_data = load_yaml(MODEL_DIR / "e007_rs485.yaml")

# Normalize to lists
devices = devices_data.get("devices", [])
wires = wires_data.get("wires", [])
nodes_allowlist = wires_data.get("nodes", [])
e007_links = e007_data.get("links", [])
open_items = open_items_data.get("items", [])

# ============================================================================
# Parse SVG files to find data-wire attributes and dashing
# ============================================================================


def parse_svg_wires(svg_path):
    """
    Parse SVG and return dict: wire_label -> {'rendered': 'dashed'|'solid'|'absent', 'tagged': bool}
    Looks for data-wire attributes and checks for stroke-dasharray or data-dashed.
    """
    if not svg_path.exists():
        print(f"[WARN] {svg_path.name} not found", file=sys.stderr)
        return {}

    with open(svg_path, "r", encoding="utf-8") as f:
        content = f.read()

    # Find all XML elements containing data-wire attribute
    # Check if element also contains stroke-dasharray (dashed marker)
    result = {}

    # Match complete element tags: < ... data-wire="..." ... >
    for element_match in re.finditer(r'<[^>]*data-wire="([^"]+)"[^>]*>', content):
        wire_label = element_match.group(1)
        element = element_match.group(0)

        # Check if element contains stroke-dasharray or data-dashed
        is_dashed = "stroke-dasharray" in element or "data-dashed" in element

        result[wire_label] = {"rendered": "dashed" if is_dashed else "solid", "tagged": True}

    return result


svg_wires = {
    "E-003": parse_svg_wires(SHEETS_DIR / "E-003_vfd_power.svg"),
    "E-005": parse_svg_wires(SHEETS_DIR / "E-005_plc_inputs.svg"),
    "E-006": parse_svg_wires(SHEETS_DIR / "E-006_plc_outputs.svg"),
    "E-007": parse_svg_wires(SHEETS_DIR / "E-007_rs485_modbus.svg"),
}

