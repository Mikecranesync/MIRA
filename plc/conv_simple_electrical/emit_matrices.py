def build_field_verify_list():
    """Build meter-check docket."""

    lines = [
        "# FIELD VERIFY LIST",
        "",
        f"> {HEADER}",
        "",
        "The docket of everything NOT yet verified. Every wire with status ≠ verified, "
        "and every terminal marked field_verify.",
        "",
    ]

    # Wires by sheet
    for sheet_id in ["E-003", "E-005", "E-006", "E-007"]:
        if sheet_id == "E-007":
            sheet_wires = e007_links
            key_field = "wire_label"
        else:
            sheet_wires = [w for w in wires if w.get("sheet") == sheet_id]
            key_field = "proposed_number"

        # Filter to non-verified
        to_verify = [
            w
            for w in sheet_wires
            if w.get("evidence" if sheet_id == "E-007" else "status") != "verified"
        ]
        to_verify_sorted = sorted(to_verify, key=lambda x: x.get(key_field, ""))

        if to_verify:
            lines.append(f"## {sheet_id} — Wires to Verify")
            lines.append("")

            for wire in to_verify_sorted:
                if sheet_id == "E-007":
                    wire_label = wire.get("wire_label", "?")
                    from_ep = f"{wire.get('src_device', '?')}.{wire.get('src_terminal', '?')}"
                    to_ep = f"{wire.get('dst_device', '?')}.{wire.get('dst_terminal', '?')}"
                    notes = wire.get("notes", "")
                    verify = notes if notes else "confirm conductor + endpoints"
                else:
                    wire_label = wire.get("proposed_number", "?")
                    from_ep = wire.get("from", "?")
                    to_ep = wire.get("to", "?")
                    notes = wire.get("note", "")
                    verify = notes if notes else "confirm conductor + endpoints + wire number"

                lines.append(f"- **{wire_label}**: {from_ep} → {to_ep}")
                lines.append(f"  - Verify: {verify}")
                lines.append("")

    # Field-verify terminals per sheet
    for sheet_id in ["E-003", "E-005", "E-006", "E-007"]:
        # Find devices on this sheet
        sheet_devices = set()
        for wire in wires:
            if wire.get("sheet") == sheet_id:
                from_ep = wire.get("from", "")
                to_ep = wire.get("to", "")
                if "." in from_ep:
                    sheet_devices.add(from_ep.split(".")[0])
                if "." in to_ep:
                    sheet_devices.add(to_ep.split(".")[0])

        if sheet_id == "E-007":
            for link in e007_links:
                sheet_devices.add(link.get("src_device", ""))
                sheet_devices.add(link.get("dst_device", ""))

        # Find field_verify terminals on these devices
        fv_terminals = []
        for term_key, term_data in terminals_flat.items():
            device_tag = term_key.split(".")[0]
            if device_tag in sheet_devices and term_data.get("status") == "field_verify":
                fv_terminals.append((term_key, term_data))

        if fv_terminals:
            lines.append(f"## {sheet_id} — Terminals to Verify")
            lines.append("")
            for term_key, term_data in sorted(fv_terminals):
                function = term_data.get("function", "")
                lines.append(f"- **{term_key}**: {function}")
                lines.append("")

    # Open items table
    lines.append("## Open Items")
    lines.append("")
    lines.append("| OI ID | Sheet | Item | Verify |")
    lines.append("|-------|-------|------|--------|")

    oi_sorted = sorted(open_items, key=lambda x: x.get("id", ""))
    for item in oi_sorted:
        oi_id = item.get("id", "?")
        sheet = item.get("sheet", "?")
        item_text = item.get("item", "")
        verify = item.get("verify", "")
        lines.append(
            f"| {oi_id} | {sheet} | {item_text[:50] if len(item_text) > 50 else item_text} | "
            f"{verify[:50] if len(verify) > 50 else verify} |"
        )

    lines.append("")

    # Summary counts
    fv_wires_count = sum(1 for w in wires if w.get("status") != "verified")
    fv_wires_count += sum(1 for link in e007_links if link.get("evidence") != "verified")
    fv_terminals_count = sum(
        1 for t in terminals_flat.values() if t.get("status") == "field_verify"
    )
    oi_count = len(open_items)

    lines.append("### Summary")
    lines.append(f"- Total field-verify wires: {fv_wires_count}")
    lines.append(f"- Total field-verify terminals: {fv_terminals_count}")
    lines.append(f"- Total open items: {oi_count}")
    lines.append("")

    return "\n".join(lines)


# ============================================================================
# Write files
# ============================================================================

try:
    evidence_matrix = build_evidence_matrix()
    crossref_matrix = build_crossref_matrix()
    field_verify_list = build_field_verify_list()

    evidence_path = REVIEW_DIR / "EVIDENCE_MATRIX.md"
    crossref_path = REVIEW_DIR / "CROSSREF_MATRIX.md"
    fv_path = REVIEW_DIR / "FIELD_VERIFY_LIST.md"

    with open(evidence_path, "w", encoding="utf-8") as f:
        f.write(evidence_matrix)
    with open(crossref_path, "w", encoding="utf-8") as f:
        f.write(crossref_matrix)
    with open(fv_path, "w", encoding="utf-8") as f:
        f.write(field_verify_list)

    print(f"[OK] {evidence_path}")
    print(f"[OK] {crossref_path}")
    print(f"[OK] {fv_path}")

    sys.exit(0)

except Exception as e:
    print(f"ERROR: {e}", file=sys.stderr)
    import traceback

    traceback.print_exc()
    sys.exit(1)
