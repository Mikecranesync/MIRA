def build_crossref_matrix():
    """Build terminal and device cross-reference."""

    lines = [
        "# CROSSREF MATRIX",
        "",
        f"> {HEADER}",
        "",
    ]

    # Device table
    lines.append("## Devices")
    lines.append("")
    lines.append("| Tag | Type | Evidence | Role |")
    lines.append("|-----|------|----------|------|")

    devices_sorted = sorted(devices, key=lambda x: x.get("tag", ""))
    for device in devices_sorted:
        tag = device.get("tag", "?")
        dev_type = device.get("type", "")
        evidence = device.get("evidence", "")
        role = device.get("role", "")
        # Truncate role at 90 chars
        if len(role) > 90:
            role = role[:87] + "..."
        lines.append(f"| {tag} | {dev_type} | {evidence} | {role} |")

    lines.append("")

    # Terminal table
    lines.append("## Terminals")
    lines.append("")
    lines.append("| Device.Terminal | Function | Status | Wires Touching | Sheets |")
    lines.append("|---|---|---|---|---|")

    # Build wire endpoint index
    wire_endpoints = defaultdict(list)
    for wire in wires:
        from_ep = wire.get("from", "")
        to_ep = wire.get("to", "")
        sheet = wire.get("sheet", "")
        wire_num = wire.get("proposed_number", "?")

        # Only match endpoints that look like "TAG.TERMINAL"
        if "." in from_ep and from_ep not in nodes_allowlist:
            wire_endpoints[from_ep].append((wire_num, sheet))
        if "." in to_ep and to_ep not in nodes_allowlist:
            wire_endpoints[to_ep].append((wire_num, sheet))

    # Add E-007 endpoints
    for link in e007_links:
        src_device = link.get("src_device", "")
        src_terminal = link.get("src_terminal", "")
        dst_device = link.get("dst_device", "")
        dst_terminal = link.get("dst_terminal", "")
        wire_label = link.get("wire_label", "?")
        sheet = e007_data.get("sheet", "E-007")

        src_ep = f"{src_device}.{src_terminal}"
        dst_ep = f"{dst_device}.{dst_terminal}"

        wire_endpoints[src_ep].append((wire_label, sheet))
        wire_endpoints[dst_ep].append((wire_label, sheet))

    # Flatten and sort terminals
    terminals_sorted = sorted(terminals_flat.items())

    for term_key, term_data in terminals_sorted:
        function = term_data.get("function", "")
        status = term_data.get("status", "")
        touching = wire_endpoints.get(term_key, [])

        # Build wire list and sheet set
        if touching:
            wire_list = ", ".join(sorted(set(w[0] for w in touching)))
            sheet_set = sorted(set(w[1] for w in touching))
            sheets_str = ", ".join(sheet_set)
        else:
            wire_list = "—"
            sheets_str = "—"

        lines.append(f"| {term_key} | {function} | {status} | {wire_list} | {sheets_str} |")

    lines.append("")

    # Pseudo-nodes
    lines.append("## Pseudo-Nodes (Rails, Buses, Off-Sheet)")
    lines.append("")
    lines.append("| Node | Wires Touching |")
    lines.append("|------|--------|")

    node_wires = defaultdict(list)
    for wire in wires:
        from_ep = wire.get("from", "")
        to_ep = wire.get("to", "")
        wire_num = wire.get("proposed_number", "?")

        if from_ep in nodes_allowlist:
            node_wires[from_ep].append(wire_num)
        if to_ep in nodes_allowlist:
            node_wires[to_ep].append(wire_num)

    for node in sorted(nodes_allowlist):
        wires_touching = sorted(set(node_wires.get(node, [])))
        wire_list = ", ".join(wires_touching) if wires_touching else "—"
        lines.append(f"| {node} | {wire_list} |")

    lines.append("")

    # Orphans check
    lines.append("## Orphans (Unresolved Endpoints)")
    lines.append("")

    orphans = set()
    for wire in wires:
        from_ep = wire.get("from", "")
        to_ep = wire.get("to", "")

        # Check if from_ep is resolvable
        if not (from_ep in nodes_allowlist or from_ep in terminals_flat):
            orphans.add(from_ep)
        # Check if to_ep is resolvable
        if not (to_ep in nodes_allowlist or to_ep in terminals_flat):
            orphans.add(to_ep)

    if orphans:
        lines.append("**ALERT: Unresolved endpoints found:**")
        lines.append("")
        for orphan in sorted(orphans):
            lines.append(f"- {orphan}")
    else:
        lines.append("OK: No orphans (all endpoints resolved)")

    lines.append("")

    return "\n".join(lines)


