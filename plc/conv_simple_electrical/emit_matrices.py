# Build device & terminal lookup
# ============================================================================


def flatten_terminals():
    """Flatten terminals.yaml structure into a dict: 'TAG.TERMINAL_ID' -> terminal_data"""
    terminals_flat = {}

    for device_tag in terminals_data:
        if device_tag not in devices_data:
            # Create a dummy entry for devices in terminals but not devices.yaml
            pass

        device_terminals = terminals_data[device_tag]

        if isinstance(device_terminals, list):
            # Flat list (S0, SS1, S2, B1, etc.)
            for term in device_terminals:
                term_id = term.get("id", "?")
                key = f"{device_tag}.{term_id}"
                terminals_flat[key] = term
        elif isinstance(device_terminals, dict):
            # Nested structure (PLC1, VFD1, etc.)
            for section_name in device_terminals:
                section = device_terminals[section_name]
                if isinstance(section, list):
                    for term in section:
                        term_id = term.get("id", "?")
                        key = f"{device_tag}.{term_id}"
                        terminals_flat[key] = term

    return terminals_flat


terminals_flat = flatten_terminals()

