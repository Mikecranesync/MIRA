def test_sandbox_patch_fails_on_bad_diff(tmp_path: Path) -> None:
    cluster = FailureCluster(signature="cp_forbidden_keywords_hit", members=[])
    patch_proposal = PatchProposal(
        diff="not a valid diff at all",
        model="claude-sonnet-4-6",
        input_tokens=50,
    )

    proposer = FixProposer(scorecards_dir=tmp_path, repo_path=tmp_path, gh_token="")

    call_count = 0

    def mock_subprocess_run(args: list[str], **kwargs: object) -> MagicMock:
        nonlocal call_count
        r = MagicMock()
        cmd = args[0] if args else ""
        if cmd == "git" and "worktree" in args and "add" in args:
            r.returncode = 0
        elif cmd == "patch" and "--dry-run" in args:
            r.returncode = 1
            r.stderr = "patch: malformed patch"
        else:
            r.returncode = 0
        r.stdout = ""
        call_count += 1
        return r

    with patch("subprocess.run", side_effect=mock_subprocess_run):
        result = proposer.sandbox_patch(cluster, patch_proposal)

    assert result.patch_applies is False
    assert "patch --dry-run failed" in result.error


def test_sandbox_reports_regression_on_eval_failure(tmp_path: Path) -> None:
    cluster = FailureCluster(signature="cp_forbidden_keywords_hit", members=[])
    patch_proposal = PatchProposal(
        diff="--- a/f.py\n+++ b/f.py\n@@ -1,1 +1,1 @@\n-old\n+new",
        model="claude-sonnet-4-6",
        input_tokens=100,
    )
    proposer = FixProposer(scorecards_dir=tmp_path, repo_path=tmp_path, gh_token="")

    def mock_subprocess_run(args: list[str], **kwargs: object) -> MagicMock:
        r = MagicMock()
        r.stdout = ""
        r.stderr = ""
        cmd = args[0] if args else ""
        if cmd == "git" and "worktree" in args:
            r.returncode = 0
        elif cmd == "patch":
            r.returncode = 0
        elif cmd == "python3" and "run_eval.py" in " ".join(args):
            # rc=2 = crash (not 0 or 1)
            r.returncode = 2
            r.stderr = "ModuleNotFoundError: no module named 'foo'"
        else:
            r.returncode = 0
        return r

    with patch("subprocess.run", side_effect=mock_subprocess_run):
        result = proposer.sandbox_patch(cluster, patch_proposal)

    assert result.patch_applies is True
    assert result.dry_run_passed is False
    assert "rc=2" in result.error


