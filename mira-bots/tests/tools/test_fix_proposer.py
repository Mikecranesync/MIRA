def _write_scorecards(tmp_path: Path, dates: list[str]) -> None:
    for date in dates:
        (tmp_path / f"{date}.md").write_text(
            DOC_INTENT_SCORECARD_TEMPLATE.format(date=date)
        )


def _write_fixture_yaml(fixtures_dir: Path, fixture_id: str, tags: list[str]) -> None:
    fixtures_dir.mkdir(parents=True, exist_ok=True)
    data = {
        "id": fixture_id,
        "description": f"Test fixture {fixture_id}",
        "expected_final_state": "DIAGNOSIS",
        "max_turns": 5,
        "expected_keywords": ["pilz.com/support"],
        "tags": tags,
    }
    (fixtures_dir / f"11_{fixture_id}.yaml").write_text(yaml.dump(data))


