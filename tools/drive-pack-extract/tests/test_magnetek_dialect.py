"""Tests for magnetek_dialect.py — Magnetek IMPULSE fault & parameter parsers.

Tests run entirely against the SYNTHETIC fixture generated by
``_make_magnetek_sample_pdf.py`` (reportlab, reproducible, no licensed content).
The fixture covers:
- Fault pages with mnemonics, flashing flags, secondary labels, multi-code cells,
  cross-rule merges, and ambiguous-glyph cases.
- Parameter pages with dotted ids, various range formats (en-dash, tilde, hex,
  negative), starred defaults, dash units, enum sub-lines, and wrapped names.
- Negative pages (non-matching tables) to verify gate accuracy.
"""

