FIXTURE = Path(__file__).parent.parent / "fixtures" / "magnetek_sample.pdf"


@pytest.fixture(scope="session", autouse=True)
def _ensure_fixture_exists() -> None:
    """Generate the fixture if it doesn't exist."""
    if not FIXTURE.exists():
        import runpy

        maker_path = Path(__file__).parent.parent / "_make_magnetek_sample_pdf.py"
        if maker_path.exists():
            runpy.run_path(str(maker_path), run_name="__main__")


@pytest.fixture
def pdf():
    """Open the fixture PDF."""
    return pdfplumber.open(FIXTURE)


def _faults(pdf) -> list[dict]:
    """Extract all faults from all pages."""
    result = []
    for page in pdf.pages:
        result.extend(magnetek_dialect.parse_magnetek_fault_page(page))
    return result


def _params(pdf) -> list[dict]:
    """Extract all parameters from all pages."""
    result = []
    for page in pdf.pages:
        result.extend(magnetek_dialect.parse_magnetek_param_page(page))
    return result


