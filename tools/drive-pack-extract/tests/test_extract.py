def test_parse_parameters_grid_layout_recovers_name_despite_code_line_mid_span():
    """P031's Min/Max wraps across 4 physical lines, and its OWN code+name
    line renders in the MIDDLE of that span (not at the start or end) — the
    real manual's actual P031 "Motor NP Volts" layout. The name must still
    come out clean via bracket-span extraction, independent of the code
    line's position."""
    by_id = {p["parameter_id"]: p for p in _params()}
    p031 = by_id["P031"]
    assert p031["name"] == "Motor NP Volts"
    assert "nameplate" in p031["purpose"].lower()


def test_parse_parameters_grid_footnote_definition_line_does_not_corrupt_next_row():
    """A footnote-definition line between P042 and P031 glues a bracket
    reference to another parameter's id with no space ("A535[Motor") — this
    must not swallow P031's own "[Motor NP Volts]" bracket into one giant
    garbled span."""
    by_id = {p["parameter_id"]: p for p in _params()}
    assert by_id["P031"]["name"] == "Motor NP Volts"
    assert "A535" not in by_id["P031"]["name"]
    assert "Fdbk" not in by_id["P031"]["name"]


def test_parse_parameters_grid_worded_range_and_default_kept_honest_not_fabricated():
    """P031's Min/Max is a compound, non-numeric, per-voltage-class range
    ("10V (for 200V Drives), 20V (for 400V Drives), 25V (for 600V
    Drives)/Drive Rated Volts") and its Default is a worded phrase ("Based on
    Drive Rating") rather than a number — the real manual's actual P031 row
    (confirmed on a real-manual run: P031/P033/P034/P037/C121-123/C125/C127-
    128 all carry a worded or conditional default with no clean numeric
    range). Both must come out HONEST: ``range=None`` (never a fabricated
    numeric guess pulled from the surrounding prose) and ``default`` equal to
    the worded phrase verbatim (never silently dropped, never coerced into a
    number)."""
    by_id = {p["parameter_id"]: p for p in _params()}
    p031 = by_id["P031"]
    assert p031["range"] is None
    assert p031["default"] == "Based on Drive Rating"


