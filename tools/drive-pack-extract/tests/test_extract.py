"""Tests for the drive-pack manual extractor (PR-A, hardened).

Runs entirely against the SYNTHETIC fixture
(``tools/drive-pack-extract/fixtures/pf_sample.pdf``, generated by
``_make_pf_sample_pdf.py``) — the real PowerFlex manual is never read in CI
and is not present in this repo. The fixture reproduces the real manual's
MEASURED messiness (position-only column separation, a row's Description
rendering on a different physical line than its own code, a footnote paren
glued onto a code/Fault-Type, a shared group whose Fault-Type value floats
mid-group, a fault->parameter cross-reference rendering above its own code
line, a Min/Max column wrapping across 4 lines with the code line landing in
the middle, and the real manual's quoted-option-with-inline-"(Default)" enum
shape) — see ``_make_pf_sample_pdf.py``'s module docstring for the full list.
"""

from __future__ import annotations

from pathlib import Path

import cite_integrity
import extractor

FIXTURE = Path(__file__).parent.parent / "fixtures" / "pf_sample.pdf"


def _faults():
    return extractor.parse_faults(FIXTURE)


def _params():
    return extractor.parse_parameters(FIXTURE)


# ---------------------------------------------------------------------------
# Fault parser — position-aware Name/Fault-Type recovery
# ---------------------------------------------------------------------------


def test_parse_faults_extracts_known_codes_with_clean_name():
    by_id = {f["fault_id"]: f for f in _faults()}

    f004 = by_id["F004"]
    assert f004["code"] == 4
    assert f004["name"] == "UnderVoltage"
    assert f004["fault_type"] == "1"
    assert f004["page"] == 1
    # The row's Description rendered on a DIFFERENT physical line than the
    # code+name+type line (the real manual's actual F004 layout) — proving
    # the name came out clean means the column bins didn't bleed together.
    assert "1" not in f004["name"]
    assert "DC bus" not in f004["name"]


def test_parse_faults_strips_footnote_paren_glued_to_code():
    by_id = {f["fault_id"]: f for f in _faults()}
    f015 = by_id["F015"]
    assert f015["fault_id"] == "F015"  # not "F015(3)"
    assert f015["name"] == "Load Loss"
    assert f015["fault_type"] == "2"


def test_parse_faults_strips_footnote_paren_glued_to_fault_type():
    by_id = {f["fault_id"]: f for f in _faults()}
    f013 = by_id["F013"]
    assert f013["name"] == "Ground Fault"
    assert f013["fault_type"] == "1"  # not "1(2)"


def test_parse_faults_handles_em_dash_fault_type():
    by_id = {f["fault_id"]: f for f in _faults()}
    f000 = by_id["F000"]
    assert f000["name"] == "No Fault"
    assert f000["fault_type"] == "—"


def test_parse_faults_shared_group_yields_one_entry_per_code():
    faults = _faults()
    codes = [f["fault_id"] for f in faults]
    for code in ("F038", "F039", "F040"):
        assert codes.count(code) == 1, f"expected exactly one entry for {code}"


def test_every_fault_row_has_a_recovered_fault_type_not_none():
    """Regression for the real-manual bug where scoping by RE-SLICING pages
    into a new PDF (rather than selecting pages by number from the original)
    reordered pages and destroyed the per-page column geometry the Fault-Type
    column depends on — every fault_type came back ``None``. Every fault in
    the fixture must carry SOME recovered value (a real "1"/"2"/em-dash),
    never ``None`` or an empty string."""
    for fault in _faults():
        assert fault["fault_type"], f"{fault['fault_id']} has no recovered fault_type"


def test_parse_faults_shared_group_fault_type_floats_mid_group():
    """Regression: the real manual sometimes renders a shared group's
    Fault-Type value BETWEEN member lines (closing after the 2nd member) —
    the trailing 3rd member (F040) must still inherit the same type, not get
    swallowed into whatever unrelated fault happens to close next."""
    by_id = {f["fault_id"]: f for f in _faults()}
    for code, name in (
        ("F038", "Phase U to Gnd"),
        ("F039", "Phase V to Gnd"),
        ("F040", "Phase W to Gnd"),
    ):
        assert by_id[code]["name"] == name
        assert by_id[code]["fault_type"] == "2"


# ---------------------------------------------------------------------------
# references_parameters — fault -> parameter cross-refs (correct DIRECTION)
# ---------------------------------------------------------------------------


def test_fault_references_parameters_captured_even_when_action_renders_above_code_line():
    """F081's "Modify using C125 [...]" action text renders ABOVE its own
    code line in the fixture (mirroring the real manual's F081 layout) —
    nearest-row assignment must still attribute it to F081."""
    by_id = {f["fault_id"]: f for f in _faults()}
