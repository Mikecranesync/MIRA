# ---------------------------------------------------------------------------
# related_faults semantics — THE fix: only fault codes, never a parameter id,
# and never sourced from a parameter's own "Related Parameters:" line.
# ---------------------------------------------------------------------------


def test_related_parameters_line_is_captured_separately_not_as_related_faults():
    """C125's own "Related Parameters: P045" line is a param<->param link —
    it must land in ``related_parameters``, and ``related_faults`` must
    start EMPTY (not yet linked from the fault table)."""
    by_id = {p["parameter_id"]: p for p in _params()}
    c125 = by_id["C125"]
    assert c125["related_parameters"] == ["P045"]
    assert c125["related_faults"] == []


def test_link_fault_actions_to_parameters_builds_related_faults_from_fault_action():
    """The genuine fault->param link comes from the FAULT table's action
    text ("Modify using C125 [...]"), never from a parameter's own "Related
    Parameters:" line. After linking, C125.related_faults must be the FAULT
    code(s) whose action referenced it — never "P045" (a parameter id)."""
    faults = _faults()
    params = _params()
    extractor.link_fault_actions_to_parameters(faults, params)

    by_id = {p["parameter_id"]: p for p in params}
    assert by_id["C125"]["related_faults"] == ["F081"]
    # The param<->param field is untouched by linking.
    assert by_id["C125"]["related_parameters"] == ["P045"]


def test_link_never_puts_a_parameter_id_into_related_faults():
    """Broad sanity sweep: after linking, no parameter's related_faults ever
    contains something that isn't a real fault_id from the fault table."""
    faults = _faults()
    params = _params()
    extractor.link_fault_actions_to_parameters(faults, params)

    fault_ids = {f["fault_id"] for f in faults}
    for param in params:
        for ref in param["related_faults"]:
            assert ref in fault_ids, (
                f"{param['parameter_id']}.related_faults has non-fault id {ref!r}"
            )


def test_related_faults_non_linked_entries_stay_empty_no_inference():
    faults = _faults()
    params = _params()
    extractor.link_fault_actions_to_parameters(faults, params)

    by_id = {p["parameter_id"]: p for p in params}
    assert by_id["P042"]["related_faults"] == []
    assert by_id["C124"]["related_faults"] == []


# ---------------------------------------------------------------------------
# Page-range scoping — select pages by 1-based number from the ORIGINAL pdf.
# Added after the real-manual run showed the un-scoped ``extract()`` times
# out (>280s) walking all 274 pages, and that re-slicing into a new PDF (an
# earlier, wrong fix attempt) reorders pages and destroys the per-page
# column geometry the position-aware parsers depend on — proven above by the
# Fault-Type recovery regression. The fixture has 3 pages: 1 = fault table,
# 2 = grid parameters, 3 = labeled-block parameters.
# ---------------------------------------------------------------------------


def test_parse_faults_pages_scopes_to_the_requested_page():
    scoped = extractor.parse_faults(FIXTURE, pages=[1])
    assert {f["fault_id"] for f in scoped} == {f["fault_id"] for f in _faults()}
    assert all(f["page"] == 1 for f in scoped)


def test_parse_parameters_pages_scopes_to_the_requested_pages():
    scoped = extractor.parse_parameters(FIXTURE, pages=[2, 3])
    assert {p["parameter_id"] for p in scoped} == {p["parameter_id"] for p in _params()}


def test_extract_threads_fault_pages_and_param_pages_through():
    """``extract()``'s ``fault_pages``/``param_pages`` kwargs must reach the
    underlying parsers — a scoped run must recover the same fragment as the
    whole-document (``pages=None``) default on this small fixture."""
    scoped = extractor.extract(
        FIXTURE, doc="pf_sample.pdf (synthetic)", fault_pages=[1], param_pages=[2, 3]
    )
    whole_doc = extractor.extract(FIXTURE, doc="pf_sample.pdf (synthetic)")
    assert scoped["fault_codes"] == whole_doc["fault_codes"]
    scoped_param_ids = {p["parameter_id"] for p in scoped["parameters"]}
    whole_doc_param_ids = {p["parameter_id"] for p in whole_doc["parameters"]}
    assert scoped_param_ids == whole_doc_param_ids


