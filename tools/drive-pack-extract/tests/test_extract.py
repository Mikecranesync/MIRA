def test_cite_integrity_accepts_a_real_excerpt():
    real_excerpt = "F004 UnderVoltage 1"
    assert cite_integrity.verify_excerpt_on_page(FIXTURE, 1, real_excerpt) is True


def test_cite_integrity_rejects_a_fabricated_excerpt():
    fabricated = "F999 TotallyMadeUpFault this sentence never appears in the manual"
    assert cite_integrity.verify_excerpt_on_page(FIXTURE, 1, fabricated) is False


def test_cite_integrity_rejects_real_text_claimed_on_the_wrong_page():
    real_excerpt_from_page_1 = "F004 UnderVoltage 1"
    assert cite_integrity.verify_excerpt_on_page(FIXTURE, 2, real_excerpt_from_page_1) is False


def test_cite_integrity_rejects_empty_excerpt():
    assert cite_integrity.verify_excerpt_on_page(FIXTURE, 1, "") is False
    assert cite_integrity.verify_excerpt_on_page(FIXTURE, 1, "   ") is False


def test_cite_integrity_is_tolerant_of_whitespace_normalization():
    # Same words, different internal whitespace/newlines — must still verify,
    # since extract_text() line-wrapping shouldn't break a genuine citation.
    wrapped = "F004  UnderVoltage\n1"
    assert cite_integrity.verify_excerpt_on_page(FIXTURE, 1, wrapped) is True


# ---------------------------------------------------------------------------
# The extractor DROPS entries with an unverifiable excerpt
# ---------------------------------------------------------------------------


def test_verify_and_filter_drops_fabricated_entry_keeps_real_one():
    real_entry = {
        "fault_id": "F004",
        "page": 1,
        "excerpt": "F004 UnderVoltage 1",
    }
    fabricated_entry = {
        "fault_id": "F999",
        "page": 1,
        "excerpt": "F999 TotallyMadeUpFault this text was never in the manual at all",
    }

    kept = extractor.verify_and_filter_entries(
        FIXTURE, [real_entry, fabricated_entry], label="fault"
    )

    kept_ids = [e["fault_id"] for e in kept]
    assert "F004" in kept_ids
    assert "F999" not in kept_ids
    assert len(kept) == 1


def test_extract_end_to_end_never_emits_a_fabricated_entry():
    fragment = extractor.extract(FIXTURE, doc="pf_sample.pdf (synthetic)")

    # Every real fault/param code that should have parsed is present, with a
    # clean name (no footnote digit, no bled-in description text).
    assert fragment["fault_codes"][4] == "UnderVoltage"
    assert fragment["fault_codes"][38] == "Phase U to Gnd"
    assert fragment["fault_codes"][81] == "DSI Comm Loss"
    parameter_ids = {p["parameter_id"] for p in fragment["parameters"]}
    assert {"P042", "P031", "C124", "C125"} <= parameter_ids

    # related_faults semantics hold end-to-end: C125 carries the FAULT code
    # (F081) whose action referenced it, never the "P045" param<->param link.
    by_id = {p["parameter_id"]: p for p in fragment["parameters"]}
    assert by_id["C125"]["related_faults"] == ["F081"]

    # Every citation the fragment carries must independently re-verify — the
    # extractor's own output is provably non-fabricated, not just "trust me".
    for citation in fragment["fault_citations"]:
        assert cite_integrity.verify_excerpt_on_page(
            FIXTURE, int(citation["page"]), citation["excerpt"]
        )
    for param in fragment["parameters"]:
        sc = param["source_citation"]
        assert cite_integrity.verify_excerpt_on_page(FIXTURE, int(sc["page"]), sc["excerpt"])
        assert param["provenance_tier"] == "manual_cited"


def test_fixture_is_clearly_marked_synthetic():
    """Guard against ever mistaking this fixture for a real licensed manual."""
    pages = extractor.read_pages(FIXTURE)
    full_text = " ".join(text for _, text in pages)
    assert "SYNTHETIC TEST FIXTURE" in full_text
    assert "not a licensed manual" in full_text
