"""Build anonymized judge evidence packages for the Print Translator benchmark.

Deterministic, re-runnable, NO new inference. Reads the preserved campaign results
(`docs/eval/print-translator-campaign/results/<id>.gate_bypassed.json`) and the corpus manifest
(`docs/eval/print-translator-campaign/corpus_manifest.md`) and writes, per first-10 case:

    docs/eval/print-translator-benchmark/evidence/<case_id>.json        (shown to the judge)
    docs/eval/print-translator-benchmark/evidence/<case_id>.meta.json   (NOT shown to the judge)

Honesty contract:
    - Every field in evidence/<id>.json traces to a preserved results JSON or the corpus
      manifest. Nothing here is invented or re-generated by inference.
    - The evidence package never contains "MIRA", "Print Translator", or any translator/model
      version string — see JUDGE_PROTOCOL.md "Anonymization". That provenance lives ONLY in the
      sibling .meta.json file.
    - image_path points at the LOCAL Downloads copy of the print image
      (C:/Users/hharp/Downloads/print-translator-eval-images/<id>.png) and is never copied into
      the repo (see .claude/rules — no committing customer/OEM PDFs or bulk rendered pages).

Usage (from repo root):
    python tools/print_translator_eval/benchmark/build_evidence.py
"""

from __future__ import annotations

import json
import re
from pathlib import Path

_HERE = Path(__file__).resolve()
_REPO_ROOT = _HERE.parents[3]
_CAMPAIGN_DIR = _REPO_ROOT / "docs" / "eval" / "print-translator-campaign"
_RESULTS_DIR = _CAMPAIGN_DIR / "results"
_MANIFEST_PATH = _CAMPAIGN_DIR / "corpus_manifest.md"
_BENCH_DIR = _REPO_ROOT / "docs" / "eval" / "print-translator-benchmark"
_EVIDENCE_DIR = _BENCH_DIR / "evidence"

# LOCAL image cache — never copied into the repo (per task instructions).
_IMAGES_DIR = Path("C:/Users/hharp/Downloads/print-translator-eval-images")

# The 10 cases with a real gate-bypassed translator explanation (README.md "first-10 evaluation
# set"). Zero-padded ids, matching results/<id>.gate_bypassed.json and images/<id>.png.
FIRST_10_IDS = ["03", "05", "07", "09", "13", "14", "17", "18", "20", "25"]

# Provenance recorded in the .meta.json (NOT the judge-facing evidence). Determined by
# git-log inspection (see benchmark harness build report): every gate_bypassed run in this
# set executed BEFORE the classifier fix (PR #2608, commit c6e7c9b0) landed — the runs used
# the PRE-FIX vision_worker.py (parent commit 04a62c9c), which is exactly why gate_bypass.py
# had to skip the (still-broken) gate. print_translator.py itself was last touched by the
# original feature commit (47717ccb) and is unchanged through the fix and the docs commit.
_CLASSIFIER_COMMIT_AT_RUN_TIME = "04a62c9c19804140c18796f1cf6c2a1ba1d9b5d1"
_TRANSLATOR_VERSION = "47717ccb4ab4265211bb33cde0f031c544ebce40"


def _parse_manifest_standards(manifest_path: Path) -> dict[str, str]:
    """Parse the corpus_manifest.md "Corpus Table" and return {id (unpadded str): standard}.

    The table's first column is the manifest row number (1..25), which is the same id used
    throughout the campaign (results/<id>.json etc). Column order (from the header row):
    # | OEM | Document | URL | Page/Section | Print Type | Standard | Category | ... .
    """
    text = manifest_path.read_text(encoding="utf-8")
    standards: dict[str, str] = {}
    row_re = re.compile(r"^\|\s*(\d+)\s*\|(.*)\|\s*$")
    for line in text.splitlines():
        m = row_re.match(line.strip())
        if not m:
            continue
        row_id = m.group(1)
        cells = [c.strip() for c in m.group(2).split("|")]
        # cells: OEM, Document, URL, Page/Section, Print Type, Standard, Category, ...
        if len(cells) < 6:
            continue
        standard = cells[5]
        if not standard or standard.startswith("---"):
            continue
        standards[row_id] = standard
    return standards


def _load_gate_bypassed(case_id: str) -> dict:
    path = _RESULTS_DIR / f"{case_id}.gate_bypassed.json"
    if not path.exists():
        raise FileNotFoundError(f"Missing preserved result: {path}")
    return json.loads(path.read_text(encoding="utf-8"))


def build_case(case_id: str, standards_by_id: dict[str, str]) -> tuple[dict, dict]:
    record = _load_gate_bypassed(case_id)
    if record.get("inference") != "ran" or "translator_reply" not in record:
        raise ValueError(
            f"case {case_id}: preserved result has no real translator_reply "
            f"(inference={record.get('inference')!r}) — refusing to fabricate an evidence "
            "package for a case with no genuine candidate response."
        )

    unpadded_id = str(int(case_id))
    standard = standards_by_id.get(unpadded_id, "unknown")

    image_path = _IMAGES_DIR / f"{case_id}.png"

    evidence = {
        "case_id": case_id,
        "image_path": str(image_path),
        "oem": record["oem"],
        "document": record["document"],
        "source_url": record["url"],
        "page": record["rendered_page_number"],
        "category": record["category"],
        "standard": standard,
        "question": record["caption_submitted"],
        "candidate_response": record["translator_reply"],
        "context_notes": (
            "This candidate had NO OCR text extracted — it was grounded on the image only "
            f"(ocr_grounding={record.get('ocr_grounding', 'unknown')!r}). Judge whether it "
            "correctly separates what is visibly readable from inference accordingly: do not "
            "penalize it for declining to cite a specific tag/wire-number string it could not "
            "have OCR'd, but do penalize it under evidence_grounding_unsupported_claims if it "
            "invents a specific tag/wire-number/label that is not actually legible in the image."
        ),
    }

    meta = {
        "case_id": case_id,
        "translator": "mira_print_translator",
        "translator_version": _TRANSLATOR_VERSION,
        "classifier_commit": _CLASSIFIER_COMMIT_AT_RUN_TIME,
        "runner": "tools/print_translator_eval/gate_bypass.py",
        "execution_path": record.get("mode", "unknown"),
        "timestamp": record.get("run_timestamp_utc"),
        "ocr_grounding": record.get("ocr_grounding", "unknown"),
    }

    return evidence, meta


def main() -> int:
    _EVIDENCE_DIR.mkdir(parents=True, exist_ok=True)
    standards_by_id = _parse_manifest_standards(_MANIFEST_PATH)

    written = []
    missing_images = []
    for case_id in FIRST_10_IDS:
        evidence, meta = build_case(case_id, standards_by_id)

        image_path = Path(evidence["image_path"])
        if not image_path.exists():
            missing_images.append(str(image_path))

        evidence_path = _EVIDENCE_DIR / f"{case_id}.json"
        meta_path = _EVIDENCE_DIR / f"{case_id}.meta.json"
        evidence_path.write_text(
            json.dumps(evidence, indent=2, ensure_ascii=False) + "\n", encoding="utf-8"
        )
        meta_path.write_text(
            json.dumps(meta, indent=2, ensure_ascii=False) + "\n", encoding="utf-8"
        )
        written.append(case_id)

    print(f"Wrote {len(written)} evidence packages + {len(written)} meta files to {_EVIDENCE_DIR}")
    print(f"Case ids: {', '.join(written)}")
    if missing_images:
        print("WARNING — local image not found for:")
        for p in missing_images:
            print(f"  {p}")
    else:
        print(f"All {len(written)} local images confirmed present under {_IMAGES_DIR}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
