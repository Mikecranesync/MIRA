"""MIRA LLM-as-Judge — cross-model quality grading for eval scenarios.

Routes judge calls AWAY from the provider that generated the response:
  - Claude-generated  → judge with Groq (fallback: Gemini if GEMINI_API_KEY live)
  - Groq/Cerebras/Gemini-generated → judge with Claude
  - unknown           → Claude first (highest-quality judge), Groq fallback

Five dimensions on a 1–5 Likert scale:
  groundedness         — does response reflect KB chunks or admit no KB (no invented facts)?
  helpfulness          — would a technician on-site actually be able to act on this?
  tone                 — concise, confident, appropriate for industrial maintenance context?
  instruction_following — did response honor the user's actual ask?
  conversational_flow  — did MIRA advance FSM state logically, ask ≤1 question per turn, avoid repeating itself?

Scoring rubric:
  5 = Excellent — fully meets the dimension criteria
  4 = Good — minor gap
  3 = Acceptable — partially meets criteria
  2 = Poor — mostly fails
  1 = Very Poor / inverted — completely fails or actively harmful

Special rules:
  - Model correctly admits no KB coverage when KB was needed → groundedness = 5 (appropriate humility)
  - Model invents facts not in retrieved chunks → groundedness = 1 or 2

Environment:
  EVAL_DISABLE_JUDGE    Set to "1" to skip all judge calls (hourly cheap eval mode).
  GROQ_API_KEY          Required for judge calls when response generated by Claude.
  ANTHROPIC_API_KEY     Required for judge calls when response generated by Groq/Cerebras.
  GEMINI_API_KEY        Optional Gemini judge (if key is live — currently blocked as of 2026-04-14).
  GROQ_MODEL            Groq judge model (default: llama-3.3-70b-versatile).
  CLAUDE_MODEL          Claude judge model (default: claude-sonnet-4-6).
"""

from __future__ import annotations

import json
import logging
import os
import re
from dataclasses import asdict, dataclass, field

import httpx

logger = logging.getLogger("mira-eval-judge")

# ── Provider constants ────────────────────────────────────────────────────────

_GROQ_URL = "https://api.groq.com/openai/v1/chat/completions"
_ANTHROPIC_URL = "https://api.anthropic.com/v1/messages"
_ANTHROPIC_VERSION = "2023-06-01"
_GEMINI_URL = "https://generativelanguage.googleapis.com/v1beta/openai/chat/completions"

DIMENSIONS = ("groundedness", "helpfulness", "tone", "instruction_following", "conversational_flow")

# ── Judge prompt ──────────────────────────────────────────────────────────────

_JUDGE_PROMPT = """\
You are grading a maintenance-technician AI assistant's response. Be strict but fair.

{conversation_history_section}\
Context — what the user asked (final turn):
{user_question}

Retrieved knowledge base chunks (may be empty — model had no KB coverage):
{rag_context}

Assistant's final response to grade:
{response_text}

Grade on five dimensions. Each dimension gets an integer score 1–5 and a one-sentence justification.

Rubric:
  5 = Excellent — fully meets the dimension criteria
  4 = Good — minor gap
  3 = Acceptable — partially meets criteria
  2 = Poor — mostly fails
  1 = Very Poor / inverted — completely fails or actively harmful

Dimension definitions:
  groundedness: Does the response reflect the retrieved KB chunks (or explicitly admit no KB \
coverage), with no invented facts? If the model correctly admits it lacks KB coverage and KB \
coverage was needed, score groundedness=5 (appropriate humility). If the model invents facts \
not in retrieved chunks, score groundedness=1 or 2.
  helpfulness: Would a technician on-site actually be able to act on this response? Does it \
provide concrete next steps, fault codes, parameter values, or vendor info they can use right now?
  tone: Is the tone appropriate for industrial maintenance — concise, confident, not condescending, \
not cheerful AI-assistant fluff? Does it treat the technician as a professional?
  instruction_following: Did the response honor the user's actual ask? For example, if the user \
asked "find me a manual", did the response provide a manual-seeking answer (vendor URL, doc \
reference) rather than deflecting with another diagnostic question?
  conversational_flow: Did MIRA advance the FSM state logically turn-by-turn (not stall, not \
skip, not repeat the same question)? Did each turn contain exactly one question? Did the \
conversation progress toward diagnosis rather than looping?

Return ONLY valid JSON — no markdown, no explanation outside the JSON:
{{
  "groundedness": {{"score": <1-5>, "note": "<one sentence>"}},
  "helpfulness": {{"score": <1-5>, "note": "<one sentence>"}},
  "tone": {{"score": <1-5>, "note": "<one sentence>"}},
  "instruction_following": {{"score": <1-5>, "note": "<one sentence>"}},
  "conversational_flow": {{"score": <1-5>, "note": "<one sentence>"}}
}}"""


# ── Data structures ───────────────────────────────────────────────────────────


@dataclass
class JudgeResult:
    """Result of a single judge call for one scenario."""

    scenario_id: str
    judge_model: str
    judge_provider: str
    scores: dict[str, int] = field(default_factory=dict)
    notes: dict[str, str] = field(default_factory=dict)
    error: str | None = None

    @property
    def total(self) -> int:
        return sum(self.scores.values())

    @property
    def average(self) -> float:
        if not self.scores:
            return 0.0
        return self.total / len(self.scores)

    @property
    def succeeded(self) -> bool:
        return self.error is None and bool(self.scores)

    def to_dict(self) -> dict:
        return asdict(self)


# ── Judge class ───────────────────────────────────────────────────────────────


class Judge:
    """Cross-model LLM judge for MIRA eval scenarios.

    Instantiate once per eval run.  Call ``grade()`` for each scenario.
    Thread-safe (all state is read-only after __init__).
    """

    def __init__(self) -> None:
        self.disabled = os.getenv("EVAL_DISABLE_JUDGE", "0") == "1"
        if self.disabled:
            logger.info("Judge: disabled via EVAL_DISABLE_JUDGE=1")
            return

        self._groq_key = os.getenv("GROQ_API_KEY", "")
        self._groq_model = os.getenv("GROQ_MODEL", "llama-3.3-70b-versatile")
        self._anthropic_key = os.getenv("ANTHROPIC_API_KEY", "")
        self._claude_model = os.getenv("CLAUDE_MODEL", "claude-sonnet-4-6")
        self._gemini_key = os.getenv("GEMINI_API_KEY", "")
        self._gemini_model = os.getenv("GEMINI_MODEL", "gemini-2.5-flash")

        available = []
        if self._groq_key:
            available.append(f"groq:{self._groq_model}")
        if self._anthropic_key:
            available.append(f"claude:{self._claude_model}")
        if self._gemini_key:
            available.append(f"gemini:{self._gemini_model}")

        if not available:
            logger.warning("Judge: no API keys set — judge calls will return errors")
        else:
            logger.info("Judge providers available: %s", ", ".join(available))

    def _pick_judge_provider(self, generated_by: str) -> str | None:
        """Pick a judge provider that is NOT the generator.

        Routing rules (mirrors ADR-0010):
          - Claude-generated  → Groq first, Gemini fallback
          - Groq-generated    → Claude
          - Cerebras-generated → Claude
          - Gemini-generated  → Claude
          - unknown           → Claude (highest-quality judge), Groq fallback

        Returns provider name ("groq", "claude", "gemini") or None if unavailable.
        """
        src = (generated_by or "unknown").lower()

        if "claude" in src:
            if self._groq_key:
                return "groq"
            if self._gemini_key:
                return "gemini"
            return None

        # groq / cerebras / gemini / unknown → prefer Claude
        if self._anthropic_key:
            return "claude"
        # If Claude unavailable and source is not Groq, try Groq
        if self._groq_key and "groq" not in src:
            return "groq"
        return None

    # ── Provider API calls (sync — eval runner is script, not async service) ──

    def _call_groq(self, prompt: str) -> str:
        payload = {
            "model": self._groq_model,
            "messages": [{"role": "user", "content": prompt}],
            "max_tokens": 512,
            "temperature": 0.1,
        }
        with httpx.Client(timeout=30) as client:
            resp = client.post(
                _GROQ_URL,
                json=payload,
                headers={"Authorization": f"Bearer {self._groq_key}"},
            )
            resp.raise_for_status()
        return resp.json()["choices"][0]["message"]["content"]

    def _call_claude(self, prompt: str) -> str:
        payload = {
            "model": self._claude_model,
            "max_tokens": 512,
            "messages": [{"role": "user", "content": prompt}],
        }
        with httpx.Client(timeout=60) as client:
            resp = client.post(
                _ANTHROPIC_URL,
                json=payload,
                headers={
                    "x-api-key": self._anthropic_key,
                    "anthropic-version": _ANTHROPIC_VERSION,
                    "content-type": "application/json",
                },
            )
            resp.raise_for_status()
        return resp.json()["content"][0]["text"]

    def _call_gemini(self, prompt: str) -> str:
        payload = {
            "model": self._gemini_model,
            "messages": [{"role": "user", "content": prompt}],
            "max_tokens": 512,
            "temperature": 0.1,
        }
        with httpx.Client(timeout=30) as client:
            resp = client.post(
                f"{_GEMINI_URL}?key={self._gemini_key}",
                json=payload,
            )
            resp.raise_for_status()
        return resp.json()["choices"][0]["message"]["content"]

    def _call_provider(self, provider: str, prompt: str) -> str:
        if provider == "groq":
            return self._call_groq(prompt)
        if provider == "claude":
            return self._call_claude(prompt)
        if provider == "gemini":
            return self._call_gemini(prompt)
        raise ValueError(f"Unknown judge provider: {provider!r}")

    def _parse_json(self, text: str) -> dict:
        """Extract JSON from model response, stripping markdown fences if present."""
        text = text.strip()
        text = re.sub(r"^```(?:json)?\s*", "", text)
        text = re.sub(r"\s*```$", "", text)
        return json.loads(text.strip())

    def _validate_result(self, data: dict) -> tuple[dict[str, int], dict[str, str]]:
        """Validate and extract scores/notes from parsed JSON. Raises ValueError on bad input."""
        scores: dict[str, int] = {}
        notes: dict[str, str] = {}
        for dim in DIMENSIONS:
            if dim not in data:
                raise ValueError(f"Missing dimension: {dim!r}")
            entry = data[dim]
            score = int(entry["score"])
            if not 1 <= score <= 5:
                raise ValueError(f"Score out of range for {dim!r}: {score}")
            scores[dim] = score
            notes[dim] = str(entry.get("note", ""))
        return scores, notes

    @staticmethod
    def _build_history_section(conversation_history: list[dict] | None) -> str:
        """Format turn log into a condensed history prefix for the judge prompt."""
        if not conversation_history:
            return ""
        lines = ["Conversation history (condensed):"]
        for i, turn in enumerate(conversation_history, start=1):
            user = (turn.get("user_msg") or "")[:120]
            state = turn.get("fsm_state") or "?"
            lines.append(f"  Turn {i}: user said \"{user}\" → FSM advanced to {state}")
        lines.append("")
        return "\n".join(lines) + "\n\n"

    def grade(
        self,
        response: str,
        rag_context: str,
        user_question: str,
        generated_by: str = "unknown",
        scenario_id: str = "unknown",
        conversation_history: list[dict] | None = None,
    ) -> JudgeResult:
        """Grade a single response across five dimensions.

        Args:
            response:              The assistant's response text to grade.
            rag_context:           Retrieved KB chunks (empty string if no KB available).
            user_question:         The user's primary ask (last user turn recommended).
            generated_by:          Provider that generated the response ("claude", "groq", etc.).
            scenario_id:           Fixture ID for logging and tracking.
            conversation_history:  Optional list of {turn, user_msg, fsm_state} dicts for
                                   multi-turn context. Enables conversational_flow scoring.

        Returns:
            JudgeResult with scores, notes, and judge metadata.
            On any failure, JudgeResult.error is set and scores/notes are empty.
        """
        if self.disabled:
            return JudgeResult(
                scenario_id=scenario_id,
                judge_model="(disabled)",
                judge_provider="(disabled)",
                error="EVAL_DISABLE_JUDGE=1",
            )

        provider = self._pick_judge_provider(generated_by)
        if not provider:
            logger.error(
                "Judge: no suitable provider for generated_by=%r — skipping %s",
                generated_by,
                scenario_id,
            )
            return JudgeResult(
                scenario_id=scenario_id,
                judge_model="(unavailable)",
                judge_provider="(unavailable)",
                error=f"No judge provider available for generated_by={generated_by!r}",
            )

        judge_model = {
            "groq": self._groq_model,
            "claude": self._claude_model,
            "gemini": self._gemini_model,
        }.get(provider, provider)

        logger.info(
            "Judge: scenario=%s generated_by=%s → %s (%s)",
            scenario_id,
            generated_by,
            provider,
            judge_model,
        )

        history_section = self._build_history_section(conversation_history)
        prompt = _JUDGE_PROMPT.format(
            conversation_history_section=history_section,
            user_question=user_question or "(none)",
            rag_context=rag_context or "(no KB chunks retrieved — model had no KB coverage)",
            response_text=response or "(empty response)",
        )

        try:
            raw_text = self._call_provider(provider, prompt)
            data = self._parse_json(raw_text)
            scores, notes = self._validate_result(data)
            return JudgeResult(
                scenario_id=scenario_id,
                judge_model=judge_model,
                judge_provider=provider,
                scores=scores,
                notes=notes,
            )
        except httpx.HTTPStatusError as e:
            msg = f"HTTP {e.response.status_code}: {e.response.text[:300]}"
            logger.error("Judge HTTP error (%s): %s", provider, msg)
            return JudgeResult(
                scenario_id=scenario_id,
                judge_model=judge_model,
                judge_provider=provider,
                error=msg,
            )
        except (json.JSONDecodeError, KeyError, ValueError) as e:
            logger.error("Judge parse error (%s/%s): %s", provider, scenario_id, e)
            return JudgeResult(
                scenario_id=scenario_id,
                judge_model=judge_model,
                judge_provider=provider,
                error=f"Parse error: {e}",
            )
        except Exception as e:
            logger.error("Judge unexpected error (%s/%s): %s", provider, scenario_id, e)
            return JudgeResult(
                scenario_id=scenario_id,
                judge_model=judge_model,
                judge_provider=provider,
                error=f"Unexpected: {e}",
            )
